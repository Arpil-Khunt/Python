#Arithmatic operator => +,-,/,%,*
a = 10;
b = 10;
print("sum : ",a+b)
print("substraction : ",a-b)
print("multiplication : ",a*b)
print("division : ",a/b)
print('modulo : ',a%b)

#assignment operator => +=, -=, /=, *=, %=
a += 10;
print('check assignment : ',a)
#relational operator => ==,<,<=,>,>=
print('check is a is smaller then b :',a<b);

#logical operator => and,or,not
e = True and True #True
print('e :',e)
f = True and False #False
print('f :',f)

g = True or False #True
h = False or False #False
print('g :',g)
print('h :',h)

i = not True #False It's complement
print('i: ',i)
